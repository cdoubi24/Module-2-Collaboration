# 4.2 Matching `small` and `green` to objects

# Assigning True or False to the variables
small = True  # Example assignment
green = False  # Example assignment

# Conditional logic for matching
if small and green:
    print("pea")
elif small and not green:
    print("cherry")
elif not small and green:
    print("watermelon")
else:
    print("pumpkin")
